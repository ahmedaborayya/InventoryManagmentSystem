    private void addOrder() {
        try {
            Order order = new Order();
            order.setCustomerId(Integer.parseInt(txtOrderCustomerID.getText()));

            // Validate date format (YYYY-MM-DD)
            String orderDateString = txtOrderDate.getText();
            if (isValidDateFormat(orderDateString)) {
                order.setOrderDate(java.sql.Date.valueOf(orderDateString));  // Convert String to java.sql.Date
            } else {
                throw new IllegalArgumentException("Invalid date format. Please use YYYY-MM-DD.");
            }

            orderDAO.add(order);
            loadOrderData();
            clearOrderForm();
        } catch (Exception e) {
            e.printStackTrace();  // Print stack trace to see more details
            JOptionPane.showMessageDialog(frame, "Error adding order: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

